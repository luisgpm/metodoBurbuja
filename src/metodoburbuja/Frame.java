    // limpia todas las filas de la tabla
    public void limpiarJTable() {
        for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }
    
    // deja solo la primer fila de la tabla
    public void limpiarJTable2() {
        for (int i = modelo.getRowCount() - 1; i >= 1; i--) {
            modelo.removeRow(i);
        }
    }

