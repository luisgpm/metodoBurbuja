    private void BtnPedirNumerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPedirNumerosActionPerformed
        // TODO add your handling code here:
        limpiarJTable();
        cantidad = Integer.parseInt(JOptionPane.showInputDialog("dame la cantidad de numeros a ordenar.."));
        numeros = burbuja.pedirNumeros(numeros, cantidad);
        Object titulos[] = new Object[numeros.length+1];
        titulos[0]= "<html>columnas <br> filas </html> ";
        Object contenido[] = new Object[numeros.length+1];
        contenido[0]= 1;
        if (numeros != null) {
            for (int i = 0; i < numeros.length; i++) {
                titulos[i+1] = "" + (i + 1);
                contenido[i+1] = numeros[i];
            }
        }

        modelo.setColumnIdentifiers(titulos);
        modelo.addRow(contenido);
        btnMayorMenor.setEnabled(true);
        btnMenorAMayor.setEnabled(true);
    }//GEN-LAST:event_BtnPedirNumerosActionPerformed

